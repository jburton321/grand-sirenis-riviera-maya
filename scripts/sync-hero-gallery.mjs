/**
 * Lists home-page hero rotator images from the flat `public/images/` folder.
 * Includes long-hash WebPs used for the under-hero gallery (excludes guest-review-only suite shots, bento, etc.).
 * Run: npm run sync:hero-gallery
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const dir = path.join(root, 'public', 'images');
const outFile = path.join(root, 'src', 'content', 'heroGalleryFilenames.ts');

if (!fs.existsSync(dir)) {
  console.error('sync-hero-gallery: missing directory', dir);
  process.exit(1);
}

/** Guest-review gallery uses all `juniorsuitedeluxesingle*.webp`; hero uses only these four IDs (shared files on disk). */
const HERO_JUNIOR_IDS = /juniorsuitedeluxesingle(1593|2245|4217|6350)-/;

/** Bento tiles in flat `images/` — short filenames; do not treat as hero rotator. */
const BENTO_WEBP = new Set([
  'bars.webp',
  'beach.webp',
  'buffet.webp',
  'pool.webp',
  'spa.webp',
  'watersports.webp',
]);

function isHeroGalleryWebp(name) {
  if (!name.endsWith('.webp')) return false;
  if (BENTO_WEBP.has(name)) return false;
  if (name.startsWith('juniorsuitedeluxesingle')) {
    return HERO_JUNIOR_IDS.test(name);
  }
  return /^(beach|breathe|buffet|fotos|grand-sirenis|gsrm)/.test(name);
}

const files = fs
  .readdirSync(dir)
  .filter((f) => {
    if (f.startsWith('.') || f === 'Thumbs.db') return false;
    return isHeroGalleryWebp(f);
  })
  .sort((a, b) => a.localeCompare(b, undefined, { sensitivity: 'base' }));

const escape = (s) => s.replace(/\\/g, '\\\\').replace(/'/g, "\\'");

const body = `/**
 * Auto-generated by scripts/sync-hero-gallery.mjs — do not edit by hand.
 * Run: npm run sync:hero-gallery
 * Source: public/images/ (flat; hero WebPs only)
 */
export const HERO_GALLERY_FILENAMES = [
${files.map((f) => `  '${escape(f)}',`).join('\n')}
] as const;
`;

fs.writeFileSync(outFile, body, 'utf8');
console.log(`sync-hero-gallery: ${files.length} image(s) → ${path.relative(root, outFile)}`);
