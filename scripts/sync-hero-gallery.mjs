/**
 * Reads `public/img/hero-gallery/` and regenerates `src/content/heroGalleryFilenames.ts`.
 * Run via: npm run sync:hero-gallery (also runs before `npm run dev` / `npm run build`).
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const dir = path.join(root, 'public', 'img', 'hero-gallery');
const outFile = path.join(root, 'src', 'content', 'heroGalleryFilenames.ts');

if (!fs.existsSync(dir)) {
  console.error('sync-hero-gallery: missing directory', dir);
  process.exit(1);
}

const files = fs
  .readdirSync(dir)
  .filter((f) => {
    if (f.startsWith('.') || f === 'Thumbs.db') return false;
    return /\.(png|jpe?g|webp|avif|gif)$/i.test(f);
  })
  .sort((a, b) => a.localeCompare(b, undefined, { sensitivity: 'base' }));

const escape = (s) => s.replace(/\\/g, '\\\\').replace(/'/g, "\\'");

const body = `/**
 * Auto-generated by scripts/sync-hero-gallery.mjs — do not edit by hand.
 * Run: npm run sync:hero-gallery
 */
export const HERO_GALLERY_FILENAMES = [
${files.map((f) => `  '${escape(f)}',`).join('\n')}
] as const;
`;

fs.writeFileSync(outFile, body, 'utf8');
console.log(`sync-hero-gallery: ${files.length} image(s) → ${path.relative(root, outFile)}`);
