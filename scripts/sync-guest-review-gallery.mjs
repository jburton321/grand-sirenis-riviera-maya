/**
 * Lists guest-review carousel images from the flat `public/images/` folder
 * (all `juniorsuitedeluxesingle*.webp` files).
 * Run: npm run sync:guest-review-gallery
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const dir = path.join(root, 'public', 'images');
const outFile = path.join(root, 'src', 'content', 'guestReviewGalleryFilenames.ts');

if (!fs.existsSync(dir)) {
  console.error('sync-guest-review-gallery: missing directory', dir);
  process.exit(1);
}

const files = fs
  .readdirSync(dir)
  .filter((f) => {
    if (f.startsWith('.') || f === 'Thumbs.db') return false;
    return f.startsWith('juniorsuitedeluxesingle') && /\.(webp)$/i.test(f);
  })
  .sort((a, b) => a.localeCompare(b, undefined, { sensitivity: 'base' }));

const escape = (s) => s.replace(/\\/g, '\\\\').replace(/'/g, "\\'");

const body = `/**
 * Auto-generated by scripts/sync-guest-review-gallery.mjs - do not edit by hand.
 * Run: npm run sync:guest-review-gallery
 * Source: public/images/ (flat; juniorsuitedeluxesingle*.webp)
 */
export const GUEST_REVIEW_GALLERY_IMAGES = [
${files.map((f) => `  'images/${escape(f)}',`).join('\n')}
] as const;
`;

fs.writeFileSync(outFile, body, 'utf8');
console.log(`sync-guest-review-gallery: ${files.length} image(s) → ${path.relative(root, outFile)}`);
